# --- BÖLÜM 1: DEĞİŞKENLER ---

# GÖREV 1: 'film_adi' isminde bir değişken oluştur ve en sevdiğin filmin adını (tırnak içinde) buna eşitle.

film_adi = "matrix"

# GÖREV 2: 'imdb_puani' isminde bir değişken oluştur ve bu filme bir puan ver (Örn: 8.5).
imdb_puani = 8.5

# GÖREV 3: print() komutunu kullanarak bu iki değişkeni ekrana yazdır.
# İpucu: print(film_adi, imdb_puani) şeklinde yapabilirsin.

