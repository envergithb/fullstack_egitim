# --- BÖLÜM 2: LİSTELER (SIRALI VERİLER) ---

# GÖREV 4: 'oyuncular' isminde bir liste oluştur ve içine 3 tane oyuncu ismi yaz.
# İpucu: Köşeli parantez [] kullanmalısın. Örn: ["Ali", "Veli"]

oyuncular = ["ali", "veli", "fatima"]

# GÖREV 5: Listenin ilk elemanını (0. indeks) ekrana yazdır.

print(oyuncular[0])

# --- BÖLÜM 3: SÖZLÜKLER (DETAYLI VERİLER) ---

# GÖREV 6: 'film' isminde bir sözlük (dictionary) oluştur.
# İçinde "ad", "puan" ve "yil" anahtarları olsun. Değerlerini sen belirle.
# İpucu: Süslü parantez {} kullanmalısın. Örn: {"ad": "Matrix", "yil": 1999}

